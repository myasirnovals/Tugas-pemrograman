<?php
$bil = 1;
$bil2 = 2;

$hasil = $bil + $bil2;
echo $bil . " + " . $bil2 . " =" . $hasil . "<br>" . PHP_EOL;

$hasil = $bil - $bil2;
echo $bil . " - " . $bil2 . " = " . $hasil . "<br>" . PHP_EOL;

$hasil = $bil / $bil2;
echo $bil . " / " . $bil2 . " = " . $hasil . "<br>" . PHP_EOL;

$hasil = $bil % $bil2;
echo $bil . " % " . $bil2 . " = " . $hasil . "<br>" . PHP_EOL;

$hasil = $bil++;
echo $bil . "++ = " . $hasil . "<br>" . PHP_EOL;

$hasil = $bil2--;
echo $bil2 . "-- =" . $hasil . "<br>" . PHP_EOL;